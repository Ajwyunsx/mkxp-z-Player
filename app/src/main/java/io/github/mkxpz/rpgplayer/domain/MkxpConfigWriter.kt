private val String.rubyStringLiteral: String
    get() = "\"" + buildString {
        this@rubyStringLiteral.forEach { char ->
            when (char) {
                '\\' -> append("\\\\")
                '"' -> append("\\\"")
                '\n' -> append("\\n")
                '\r' -> append("\\r")
                '\t' -> append("\\t")
                else -> append(char)
            }
        }
    } + "\""

private fun File.writeTextIfChanged(text: String) {
    if (isFile && runCatching { readText() == text }.getOrDefault(false)) {
        return
    }
    writeText(text)
}

private fun parseIniValue(text: String, key: String): String {
    val prefix = "$key="
    return text.lineSequence()
        .firstOrNull { line -> line.trimStart().startsWith(prefix, ignoreCase = true) }
        ?.substringAfter("=")
        ?.trim()
        .orEmpty()
}
